// Lấy các món chưa hoàn thành
export const getPendingItems = (items: Item[]): Item[] => {
  return items.filter((item) => !item.isCompleted)
}

// Lấy các món đã hoàn thành
export const getCompletedItems = (items: Item[]): Item[] => {
  return items.filter((item) => item.isCompleted)
}

// Lấy các bàn theo trạng thái
export const getTablesByStatus = (tables: Table[], status: "empty" | "occupied" | "waiting"): Table[] => {
  return tables.filter((table) => table.status === status)
}

// Lấy các món theo bàn
export const getItemsByTable = (items: Item[], tableId: string): Item[] => {
  return items.filter((item) => item.tableId === tableId && !item.isCompleted)
}

